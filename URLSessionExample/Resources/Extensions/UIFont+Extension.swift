import UIKit

extension UIFont {
    class var fontStyle28Bold: UIFont {
        return UIFont.systemFont(ofSize: 28.0, weight: .bold)
    }
    
    class var fontStyle22SemiBold: UIFont {
        return UIFont.systemFont(ofSize: 22.0, weight: .semibold)
    }
}
